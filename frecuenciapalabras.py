texto = input('Digite el texto\n')
cadena = texto
a = cadena.count('a')
e = cadena.count('e')
i = cadena.count('i')
o = cadena.count('o')
u = cadena.count('u')
print('Texto:', cadena, '\n')
print(' Frecuencia de a:', a, '\n',
	'Frecuencia de e:', e,  '\n',
	'Frecuencia de i:', i, '\n',
	'Frecuencia de o:', o, '\n',
	'Frecuencia de u:', u)
